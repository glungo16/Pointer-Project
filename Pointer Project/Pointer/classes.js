// Rectangle class for canvas
class Rectangle {
	constructor(xCor, yCor, xLen, yLen) {
		this.xCor = xCor;
		this.yCor = yCor;
		this.reached = false;
		this.visible = true;
		this.xLen = xLen;
		this.yLen = yLen;
	}
	
}